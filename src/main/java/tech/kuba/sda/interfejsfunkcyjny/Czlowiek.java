package tech.kuba.sda.interfejsfunkcyjny;

public class Czlowiek {

    private String imie;
    private int wiek;

    public Czlowiek(String imie, int wiek) {
        this.imie = imie;
        this.wiek = wiek;
    }

    public String getImie() {
        return imie;
    }

    public int getWiek() {
        return wiek;
    }
}
